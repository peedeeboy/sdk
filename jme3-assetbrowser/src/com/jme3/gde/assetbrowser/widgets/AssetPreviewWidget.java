    @Override
    public void previewCreated(PreviewRequest request) {
        if (request.getRequester() == this) {
            java.awt.EventQueue.invokeLater(() -> {
                assetPreviewLabel.setIcon(IconList.asset);
//                    invalidate();
                revalidate();
                repaint();
//                    updateUI();
            });
        }
    }

