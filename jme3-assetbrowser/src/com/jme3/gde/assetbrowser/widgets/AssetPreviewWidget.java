    @Override
    public void actionPerformed(ActionEvent e) {
        switch (e.getActionCommand()) {
            case "Refresh":
                listener.refreshPreview(this);
                break;
            case "Delete":
                listener.deleteAsset(this);
                break;
        }
    }

